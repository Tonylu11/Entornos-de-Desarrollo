public class PositivaNegativaNeutraX {
	static void PositivaNegativaNeutraX(int x) {
		if (x > 0) {
			System.out.println("X es positiva.");
		} else if (x < 0) {
			System.out.println("X es negativa.");
		} else {
			System.out.println("X es neutra.");
		}
	}
}